# ── Command ───────────────────────────────────────────────────────────────────

class Command(BaseCommand):
    help = 'Populate database with realistic demo data (idempotent)'

    def add_arguments(self, parser):
        parser.add_argument('--reset', action='store_true',
                            help='Delete all existing data before seeding')

    def handle(self, *args, **options):
        if options['reset']:
            self._reset()
        if User.objects.filter(username='alice').exists():
            self.stdout.write(self.style.WARNING(
                'Database already seeded. Use --reset to reseed.'
            ))
            return

        self.stdout.write('Seeding database...')
        users = self._create_users()
        categories, tags = self._create_categories_and_tags()
        ingredients = self._create_ingredients()
        recipes = self._create_recipes(users, categories, tags, ingredients)
        self._create_social(users, recipes)
        self._create_menu_plan(users['alice'], recipes)
        self.stdout.write(self.style.SUCCESS(
            f'Done! {len(recipes)} recipes, {len(ingredients)} ingredients, '
            f'{len(COMMENTS_DATA)} comments.'
        ))

    # ── helpers ──────────────────────────────────────────────────────────────

    def _reset(self):
        from apps.social.models import Comment, Favorite, Rating
        from apps.planner.models import MenuPlan
        from apps.recipes.models import Recipe, Category, Tag, Ingredient
        Comment.objects.all().delete()
        Rating.objects.all().delete()
        Favorite.objects.all().delete()
        MenuPlan.objects.all().delete()
        Recipe.objects.all().delete()
        Category.objects.all().delete()
        Tag.objects.all().delete()
        Ingredient.objects.all().delete()
        User.objects.filter(username__in=['alice', 'bob']).delete()
        self.stdout.write('Reset complete.')

    def _make_placeholder(self, index):
        """Generate a simple coloured JPEG placeholder (400×300 px)."""
        color = _COLORS[index % len(_COLORS)]
        # convert hex to RGB
        r, g, b = int(color[1:3], 16), int(color[3:5], 16), int(color[5:7], 16)
        img = Image.new('RGB', (400, 300), (r, g, b))
        draw = ImageDraw.Draw(img)
        # draw a subtle cross-hatch pattern
        for i in range(0, 400, 40):
            draw.line([(i, 0), (i, 300)], fill=(r - 30, g - 30, b - 30), width=1)
        for j in range(0, 300, 40):
            draw.line([(0, j), (400, j)], fill=(r - 30, g - 30, b - 30), width=1)
        buf = io.BytesIO()
        img.save(buf, format='JPEG', quality=80)
        buf.seek(0)
        return buf.read()

    def _seed_image(self, index):
        """Return (filename, bytes) for a recipe's photo.

        Prefers a real photo seed_images/recipe_NN.<ext>; falls back to a
        generated coloured placeholder when the file is absent.
        """
        if SEED_IMAGES_DIR.is_dir():
            for path in sorted(SEED_IMAGES_DIR.glob(f'recipe_{index:02d}.*')):
                return path.name, path.read_bytes()
        return f'placeholder_{index:02d}.jpg', self._make_placeholder(index)

    def _assign_photo(self, recipe, index):
        """Set the recipe photo, refreshing old generated placeholders.

        Idempotent: once a real photo is attached, re-runs skip it.
        """
        if recipe.photo and 'placeholder' not in recipe.photo.name:
            return
        old_name = recipe.photo.name if recipe.photo else None
        filename, data = self._seed_image(index)
        recipe.photo.save(filename, ContentFile(data), save=True)
        if old_name and old_name != recipe.photo.name and 'placeholder' in old_name:
            recipe.photo.storage.delete(old_name)

    # ── creators ─────────────────────────────────────────────────────────────

    def _create_users(self):
        users = {}
        for data in USERS_DATA:
            user, created = User.objects.get_or_create(
                username=data['username'],
                defaults={
                    'first_name': data['first_name'],
                    'last_name': data['last_name'],
                    'email': data['email'],
                },
            )
            if created:
                user.set_password(data['password'])
                user.save()
            users[data['username']] = user
        self.stdout.write(f'  Users: {len(users)}')
        return users

    def _create_categories_and_tags(self):
        from apps.recipes.models import Category, Tag
        categories = {}
        for name, slug in CATEGORIES_DATA:
            obj, _ = Category.objects.get_or_create(slug=slug, defaults={'name': name})
            categories[name] = obj

        tags = {}
        for name, slug in TAGS_DATA:
            obj, _ = Tag.objects.get_or_create(slug=slug, defaults={'name': name})
            tags[name] = obj

        self.stdout.write(f'  Categories: {len(categories)}, Tags: {len(tags)}')
        return categories, tags

    def _create_ingredients(self):
        from apps.recipes.models import Ingredient
        ingredients = {}
        for name in INGREDIENTS_DATA:
            obj, _ = Ingredient.objects.get_or_create(name=name)
            ingredients[name] = obj
        self.stdout.write(f'  Ingredients: {len(ingredients)}')
        return ingredients

    def _create_recipes(self, users, categories, tags, ingredients):
        from apps.recipes.models import Recipe, RecipeIngredient, RecipeStep

        # Ensure media directory exists
        media_recipes = settings.MEDIA_ROOT / 'recipes'
        media_recipes.mkdir(parents=True, exist_ok=True)

        recipes = {}
        for idx, data in enumerate(RECIPES_DATA):
            recipe, created = Recipe.objects.get_or_create(
                title=data['title'],
                defaults={
                    'description': data['description'],
                    'cooking_time': data['cooking_time'],
                    'difficulty': data['difficulty'],
                    'servings': data['servings'],
                    'author': users[data['author']],
                    'is_public': data['is_public'],
                },
            )
            if created:
                # categories & tags
                recipe.categories.set(
                    [categories[c] for c in data['categories'] if c in categories]
                )
                recipe.tags.set(
                    [tags[t] for t in data['tags'] if t in tags]
                )
                # ingredients
                for ing_name, amount, unit in data['ingredients']:
                    if ing_name in ingredients:
                        RecipeIngredient.objects.get_or_create(
                            recipe=recipe,
                            ingredient=ingredients[ing_name],
                            defaults={'amount': amount, 'unit': unit},
                        )
                # steps
                for order, text in enumerate(data['steps'], start=1):
                    RecipeStep.objects.get_or_create(
                        recipe=recipe, order=order, defaults={'text': text}
                    )
            # photo: real image if available, else placeholder. Also refreshes
            # recipes that still carry an old generated placeholder.
            self._assign_photo(recipe, idx)
            recipes[data['title']] = recipe

        self.stdout.write(f'  Recipes: {len(recipes)}')
        return recipes

    def _create_social(self, users, recipes):
        from apps.social.models import Comment, Favorite, Rating

        for recipe_title, username, text in COMMENTS_DATA:
            recipe = recipes.get(recipe_title)
            user = users.get(username)
            if recipe and user:
                Comment.objects.get_or_create(
                    recipe=recipe, author=user, text=text
                )

        for recipe_title, username, value in RATINGS_DATA:
            recipe = recipes.get(recipe_title)
            user = users.get(username)
            if recipe and user:
                Rating.objects.get_or_create(
                    recipe=recipe, user=user, defaults={'value': value}
                )

        for recipe_title, username in FAVORITES_DATA:
            recipe = recipes.get(recipe_title)
            user = users.get(username)
            if recipe and user:
                Favorite.objects.get_or_create(recipe=recipe, user=user)

        self.stdout.write(
            f'  Comments: {len(COMMENTS_DATA)}, '
            f'Ratings: {len(RATINGS_DATA)}, '
            f'Favorites: {len(FAVORITES_DATA)}'
        )

    def _create_menu_plan(self, alice, recipes):
        from apps.planner.models import MealSlot, MealSlotRecipe, MenuPlan

        today = date.today()
        monday = today - timedelta(days=today.weekday())

        plan, created = MenuPlan.objects.get_or_create(user=alice, week_start=monday)
        if created:
            slots = [
                MealSlot(plan=plan, day=day, meal_type=meal_type)
                for day in range(7)
                for meal_type in ['breakfast', 'lunch', 'dinner']
            ]
            MealSlot.objects.bulk_create(slots)

        for day, meal_type, recipe_title in MENU_PLAN_DATA:
            recipe = recipes.get(recipe_title)
            if not recipe:
                continue
            slot = MealSlot.objects.filter(plan=plan, day=day, meal_type=meal_type).first()
            if not slot or slot.items.count() >= MealSlotRecipe.MAX_PER_SLOT:
                continue
            MealSlotRecipe.objects.get_or_create(
                slot=slot, recipe=recipe, defaults={'position': slot.items.count()}
            )

        self.stdout.write(f'  Menu plan: week of {monday}, {len(MENU_PLAN_DATA)} dishes placed')
